<H3Title title={"FICHIERS TÉMOINS (COOKIES)"} />

<p>Voici une liste de fichiers témoins que nous utilisons. Nous les avons énumérés ici pour que vous ayez la possibilité de choisir si vous souhaitez les
autoriser ou non.<br>
<ul>
    <li>session_id, identificateur unique de session, permet à Repertoire SEMEQ de stocker les informations relatives à votre session (référent, page de renvoi, etc.).</li>
    <li>Les « fichiers journaux » suivent l'activité du Site et recueillent des données telles que votre adresse IP, le type de navigateur que vous utilisez, votre
