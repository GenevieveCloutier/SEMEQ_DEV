<H3Title title={"COMMENT UTILISONS-NOUS VOS INFORMATIONS PERSONNELLES ?" } />

<p>En règle générale, nous utilisons les Informations sur la commande que nous recueillons pour traiter toute commande passée par le biais du Site (y
compris pour traiter vos informations de paiement, organiser l'expédition de votre commande et vous fournir des factures et/ou des confirmations de
commande). En outre, nous utilisons ces Informations sur la commande pour :<br>
<ul>
    <li>Communiquer avec vous ;</li>
    <li>Évaluer les fraudes ou risques potentiels ; </li>
    <li>Lorsque cela correspond aux préférences que vous nous avez communiquées, vous fournir des informations ou des publicités concernant nos produits ou services.</li>
</ul>

<p>Nous utilisons les Informations sur l'appareil (en particulier votre adresse IP) que nous recueillons pour évaluer les fraudes ou risques potentiels et, de
manière plus générale, pour améliorer et optimiser notre Site (par exemple, en générant des analyses sur la façon dont nos clients parcourent et
interagissent avec le Site, et pour évaluer la réussite de nos campagnes de publicité et de marketing).</p><br>

